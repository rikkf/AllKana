package com.codanimex.android.allkana.quiz;

public class KatakanaElement {
    private String katakanaKana;
    private String katakanaRomaji;

    public String getKatakanaKana() {
        return katakanaKana;
    }

    public void setKatakanaKana(String katakanaKana) {
        this.katakanaKana = katakanaKana;
    }

    public String getKatakanaRomaji() {
        return katakanaRomaji;
    }

    public void setKatakanaRomaji(String katakanaRomaji) {
        this.katakanaRomaji = katakanaRomaji;
    }
}
