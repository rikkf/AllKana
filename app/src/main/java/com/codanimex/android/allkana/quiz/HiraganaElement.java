package com.codanimex.android.allkana.quiz;

public class HiraganaElement {

    private String hiraganaKana;
    private String hiraganaRomaji;

    public String getHiraganaKana() {
        return hiraganaKana;
    }

    public void setHiraganaKana(String hiraganaKana) {
        this.hiraganaKana = hiraganaKana;
    }

    public String getHiraganaRomaji() {
        return hiraganaRomaji;
    }

    public void setHiraganaRomaji(String hiraganaRomaji) {
        this.hiraganaRomaji = hiraganaRomaji;
    }
}
